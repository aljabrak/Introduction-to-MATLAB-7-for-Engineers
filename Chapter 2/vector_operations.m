% Vector Operation.

% Scalar Addition.
a = [2, 3];
c = a + 2;
disp(c);

% Scalar Subtraction.
c = a - 2;
disp(c);

% Scalar Multiplication.
disp(3*a);

% Scalar Division.
disp(a/3);

% Vector Addition.
b = [9, 2];
c = a + b;
disp(c);

% Vector Subtraction.
c = a - b;
disp(c);

% Vector Multiplication.
c = a*b;
disp(c);

% Vector  Right Division.
c = a./b;
disp(c);

% Vector Left Division.
c = a.\b;
disp(c);

% Vector Exponent.
c = a.^b;
disp(c);
